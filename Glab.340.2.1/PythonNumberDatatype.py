# 1. Add two integers
int1 = 5
int2 = 10
int_sum = int1 + int2
print("Sum of integers:", int_sum)

# 2. Add two floats
float1 = 5.5
float2 = 10.5
float_sum = float1 + float2
print("Sum of floats:", float_sum)

